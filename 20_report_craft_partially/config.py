_CURRENT_PROJECT_FILE = OUTPUT_DIR / ".current_project"


def get_project_dir(project_name: str) -> Path:
    """
    프로젝트별 output 하위 폴더 경로 반환.
    파일명에 사용할 수 없는 문자는 '_' 로 치환한다.
    """
    safe = re.sub(r'[\\/:*?"<>|]', "_", project_name.strip())
    return OUTPUT_DIR / safe


def save_current_project(project_name: str) -> None:
    """현재 작업 중인 프로젝트명을 파일에 저장한다."""
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    _CURRENT_PROJECT_FILE.write_text(project_name.strip(), encoding="utf-8")


def load_current_project() -> str | None:
    """마지막으로 저장된 프로젝트명을 반환한다. 없으면 None."""
    if _CURRENT_PROJECT_FILE.exists():
        name = _CURRENT_PROJECT_FILE.read_text(encoding="utf-8").strip()
        return name if name else None
    return None

