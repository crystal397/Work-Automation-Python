def run_cases() -> tuple[int, int]:
    """전체 케이스 실행 → (passed, failed)."""
    passed = 0
    failed = 0
    for path, expected, label in CASES:
        hit = classify(Path(path))
        actual = hit.section
        # expected 가 "5.5" 처럼 짧으면 prefix 매칭, 그 외엔 startswith 매칭
        ok = actual.startswith(expected)
        if ok:
            passed += 1
        else:
            failed += 1
            print(f"  FAIL [{label}]")
            print(f"    path:     {path}")
            print(f"    expected: {expected}*")
            print(f"    actual:   {actual} (reason={hit.reason!r})")
    return passed, failed


def test_classifier_regression():
    passed, failed = run_cases()
    assert failed == 0, f"{failed} 케이스 실패 / {passed} 통과"


if __name__ == "__main__":
    passed, failed = run_cases()
    print(f"\n총 {passed + failed}건 / 통과 {passed} / 실패 {failed}")
    sys.exit(0 if failed == 0 else 1)
